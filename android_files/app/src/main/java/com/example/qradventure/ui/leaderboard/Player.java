package com.example.qradventure.ui.leaderboard;

public class Player {
    private String name;
    private int score;

    public Player() {}

    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
